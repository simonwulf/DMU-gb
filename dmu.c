fixed yspeed;
fixed yscroll;
UBYTE xscroll;
UBYTE vbl_counter;
UBYTE hbl_counter;
UBYTE offset;

/**
 * @param angle  Angle given as a range between 0x00 and 0xff, where 0xff is
 *               equivalent to 360 degrees.
 * @param fshift Frequency shift, used for a quick power of two multiplication
 *               of frequency. f = F * 2^fshift
 * @param ashift Amplitude shift, used for a quick power of two division of
 *               amplitude. a = A / 2^ashift
 **/
int sin(UBYTE angle, UBYTE fshift, UBYTE ashift) {
  return (int)sintable[angle << fshift] >> ashift;
}

int cos(UBYTE angle, UBYTE fshift, UBYTE ashift) {
  return (int)sintable[(angle << fshift) + 0x3f] >> ashift;
}

void vbl_handler() {

  hbl_counter = offset++;
}

void lcd_handler() {

  SCX_REG = xscroll;
  xscroll = sin(hbl_counter, 3, 4);
  hbl_counter++;
}

void main() {

  yspeed.w = 0x007f;
  yscroll.w = 0x0000;
  xscroll = 0;
  offset = 0;
  vbl_counter = 0;

  set_bkg_data(0x01, 0x30, dmu_tile_data);
  set_bkg_tiles(0, 5, 20, 8, dmu_map_data);

  STAT_REG = 0x08; // Interrupt on hblank
  disable_interrupts();
  add_VBL(vbl_handler);
  add_LCD(lcd_handler);
  enable_interrupts();

  set_interrupts(LCD_IFLAG | VBL_IFLAG);

  SHOW_BKG;
  DISPLAY_ON;

  while (1) {

    wait_vbl_done();

    //yscroll.w -= yspeed.w;
    //SCY_REG = yscroll.b.h;

    SCY_REG = sin(vbl_counter, 1, 2);
    //SCX_REG = cos(vbl_counter, 1, 2);
    vbl_counter++;
  }
}
